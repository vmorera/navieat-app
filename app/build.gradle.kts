// Optional signing config loaded from keystore.properties (NOT committed).
// In CI we set the same properties as environment variables and let GitHub
// Actions write them to a temporary keystore.properties before assembling.
val keystorePropertiesFile = rootProject.file("keystore.properties")
val keystoreProperties = Properties().apply {
    if (keystorePropertiesFile.exists()) {
        load(keystorePropertiesFile.inputStream())
    }
}

